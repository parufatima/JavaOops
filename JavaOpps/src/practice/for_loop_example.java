package practice;

public class for_loop_example {

	public static void main(String[] args) {
	
		for(int i=0; i<=20; i=i+2) {  //print even number
			System.out.println(i);
		}
    
		for(int j=0 ; j<=25; j++) {  //print all number
			System.out.println(j);
		}
		for(int k=1; k<=30; k= k+2) {  //  print all odd number
			System.out.println(k);
			
		}
		
		
	}

}
